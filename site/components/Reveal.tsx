"use client";

import { motion } from "framer-motion";
import type { ReactNode } from "react";

// Scroll-triggered reveal. Respects prefers-reduced-motion via Framer Motion's
// useReducedMotion behavior (it skips transform/opacity transitions automatically when
// the motion component's transition is disabled by the OS setting).
export function Reveal({
  children,
  delay = 0,
  className,
  as = "div",
}: {
  children: ReactNode;
  delay?: number;
  className?: string;
  as?: "div" | "section" | "li" | "span";
}) {
  const MotionTag = motion[as];
  return (
    <MotionTag
      className={className}
      initial={{ opacity: 0, y: 28 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, amount: 0.2, margin: "0px 0px -8% 0px" }}
      transition={{ duration: 0.7, ease: [0.22, 1, 0.36, 1], delay }}
    >
      {children}
    </MotionTag>
  );
}
